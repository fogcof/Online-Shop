package model;

/**
 *
 * @author Admin
 */
public class OrderDetail {
    private int id;
    private Product product;
    private Order order;
    private int price;
    private int quantity;

    public OrderDetail() {
    }

    public OrderDetail(int id, Product product, Order order, int price, int quantity) {
        this.id = id;
        this.product = product;
        this.order = order;
        this.price = price;
        this.quantity = quantity;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Product getProduct() {
        return product;
    }

    public void setProduct(Product product) {
        this.product = product;
    }

    public Order getOrder() {
        return order;
    }

    public void setOrder(Order order) {
        this.order = order;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
}
